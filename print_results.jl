include("analyzer.jl")

function printSubSpaces(A)
    C, N, CT, NT = findSubSpaces(A)

    println("C(A) = ", C, "\nN(A) = ", N, "\nC(A^T) = ", CT, "\nN(A^T) = ", NT)
end

println("-----------------------------------------------------------------")
printSubSpaces(Float64[1 3.5 4.5 2.5; 0 0 0 0; 3.5 1 4.5 -2.5; 2 3 5 1])
println("-----------------------------------------------------------------")

